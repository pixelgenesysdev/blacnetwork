  <script>
    const API_TOKEN = '7imjKXBsfKmjDAepoJO0HcRtrce75zLg5Ohh1xwx';
    let cart = [];

    // Fetch products from Printful API
    fetch('https://api.printful.com/store/products', {
      headers: { 'Authorization': `Bearer ${API_TOKEN}` }
    })
    .then(response => response.json())
    .then(data => {
      const products = data.result;
      const productsDiv = document.getElementById('products');

      products.forEach(product => {
        const productDiv = document.createElement('div');
        productDiv.className = 'product';
        productDiv.innerHTML = `
          <h3>${product.name}</h3>
          <img src="${product.thumbnail_url}" alt="${product.name}" width="100">
          <p>${product.retail_price} USD</p>
          <button onclick="addToCart(${product.id}, '${product.name}', ${product.retail_price})">Add to Cart</button>
        `;
        productsDiv.appendChild(productDiv);
      });
    })
    .catch(error => {
      console.error('Error fetching products:', error);
    });

    // Add product to cart
    function addToCart(id, name, price) {
      const product = { id, name, price };
      cart.push(product);
      updateCart();
    }

    // Update cart display
    function updateCart() {
      const cartDiv = document.getElementById('cart');
      cartDiv.innerHTML = `<h2>Cart</h2>`;
      
      if (cart.length === 0) {
        cartDiv.innerHTML += '<p>Your cart is empty.</p>';
      } else {
        let total = 0;
        cart.forEach(item => {
          cartDiv.innerHTML += `<p>${item.name} - ${item.price} USD</p>`;
          total += item.price;
        });
        cartDiv.innerHTML += `<p><strong>Total: ${total} USD</strong></p>`;
        cartDiv.innerHTML += '<button onclick="checkout()">Checkout</button>';
      }
    }

    // Simulate Checkout
    function checkout() {
      if (cart.length === 0) {
        alert('Your cart is empty!');
        return;
      }
      
      const orderData = {
        recipient: {
          name: 'John Doe',
          address1: '1234 Street',
          city: 'City',
          country_code: 'US',
          zip: '12345',
          email: 'johndoe@example.com'
        },
        items: cart.map(item => ({
          variant_id: item.id,
          quantity: 1
        }))
      };

      fetch('https://api.printful.com/orders', {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${API_TOKEN}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify(orderData)
      })
      .then(response => response.json())
      .then(data => {
        if (data.code === 200) {
          alert('Order placed successfully!');
          cart = [];  // Clear cart after successful order
          updateCart(); // Update cart display
        } else {
          alert('Error placing order.');
        }
      })
      .catch(error => {
        console.error('Error placing order:', error);
        alert('Something went wrong with the order.');
      });
    }
  </script>
  
  
  
  

